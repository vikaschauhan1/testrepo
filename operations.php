<?php
/**
 * add function
 */
 
function add($x, $y){
	return $x + $y;
}
function subtract($x, $y){
	return $x - $y;
}
function multiply($x, $y){
	return $x * $y;
}
function divide($x, $y){
	return $x / $y;
}
echo add(1,2).PHP_EOL;
echo subtract(1,2).PHP_EOL;
echo multiply(1,2).PHP_EOL;
echo divide(1,2).PHP_EOL;
?>

